    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String query = jTextField1.getText().trim(); // Obtener el texto ingresado
        if (!query.isEmpty()) {
            List<String[]> resultados;
            if (query.matches("\\d+")) { // Si la consulta es un número, buscar por DNI
                resultados = CSVReaderExample.buscarPorDNI(data, query);
            } else { // Si no es un número, buscar por nombre
                resultados = CSVReaderExample.buscarPorNombre(data, query);
            }
            mostrarResultadosEnTabla(resultados);  // Mostrar los resultados en la tabla
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    /**
     * @param args the command line arguments
     */
    // Mostrar los resultados en la tabla
    private void mostrarResultadosEnTabla(List<String[]> resultados) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Limpiar tabla
        for (String[] fila : resultados) {
            model.addRow(new Object[]{fila[0], fila[1], fila[2], fila[3], fila[4], fila[5], fila[6], fila[7]});  // Añadir los datos a la tabla
        }
    }


