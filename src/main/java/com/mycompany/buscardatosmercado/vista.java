    private void configurarTabla() {
        DefaultTableModel modeloNoEditable = new DefaultTableModel(
            new Object [][] {
                {null, null, null, null, null, null, null, null},
            },
            new String [] {
                "NOMBRES Y APELLIDOS", "PAB", "PUESTO", "N°DNI", "Mercado", "PAGO 2022", "PAGÓ 2023", "PAGO 2024"
            }
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;  // Hace que ninguna celda sea editable
            }
        };
        
        jTable1.setModel(modeloNoEditable);
        
        // Ajustar el ancho de las columnas
        TableColumnModel columnModel = new NonReorderableTableColumnModel();
        for (int i = 0; i < modeloNoEditable.getColumnCount(); i++) {
            TableColumn column = new TableColumn(i);
            column.setHeaderValue(modeloNoEditable.getColumnName(i));
            column.setPreferredWidth(100);  // Ajusta el ancho inicial si es necesario
            columnModel.addColumn(column);
        }
        jTable1.setColumnModel(columnModel);
        
        // Ajustar el ancho de las columnas
        jTable1.getColumnModel().getColumn(0).setPreferredWidth(240); // NOMBRES Y APELLIDOS
        jTable1.getColumnModel().getColumn(1).setPreferredWidth(40); // PAB
        jTable1.getColumnModel().getColumn(2).setPreferredWidth(40); // PUESTO
        jTable1.getColumnModel().getColumn(3).setPreferredWidth(40); // N°DNI
        jTable1.getColumnModel().getColumn(4).setPreferredWidth(140); // Mercado
        jTable1.getColumnModel().getColumn(5).setPreferredWidth(50); // PAGO 2022
        jTable1.getColumnModel().getColumn(6).setPreferredWidth(50); // PAGÓ 2023
        jTable1.getColumnModel().getColumn(7).setPreferredWidth(50); // PAGO 2024
        
        jScrollPane1.setViewportView(jTable1);
    }
    
